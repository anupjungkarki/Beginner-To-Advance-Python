'''
Control statements?
-- Control statements are the statements that are used to control the flow of the program.
--In python we have if else and for loop as control statements.
--If else is used to control the flow of the progrma.
--Loop is used to repeat the same statement multiple times.

Syntax syntax of control statement:
if contion:
    statement
elif condition:
    statement
else:
    statement


Syntax of for loop:
for variable in sequence:
    statement

'''

# Example of if else
a = 10
b = 20

if a > b:
    print("a is greater than b")

elif a < b:
    print("a is Less than b")

else:
    print("a is equal to b")


# Example of for Loop
for i in range(10):
    print(i)

