'''
Assignment of Value to Variable in Python
In Python, assigning a value to a variable is straightforward and does not require explicit declaration of the variable type.
You simply use the assignment operator (=) to assign a value to a variable name.

'''
# Example of assigning values to variables
x = 10          # Assigning an integer value
y = 3.14        # Assigning a float value
name = "Anup"  # Assigning a string value
is_student = True  # Assigning a boolean value
# Printing the values of the variables
print("Value of x:", x)
print("Value of y:", y)
print("Value of name:", name)
print("Is student:", is_student)
# You can also reassign values to the same variable
x = 20
print("New value of x:", x)

