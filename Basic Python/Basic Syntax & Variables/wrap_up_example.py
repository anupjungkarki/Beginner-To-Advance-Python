# Another Example using eval() function to take input from user
name = input("Enter Name: ")
faculty = input("Enter Faculty: ")
marks = eval(input("Enter Marks: "))

print("Name:", name)
print("Faculty:", faculty)
print("Total Marks:", marks)
# Above we have created a simple program that takes input from the user using input() function
# and eval() function and then prints the output using print() function.
