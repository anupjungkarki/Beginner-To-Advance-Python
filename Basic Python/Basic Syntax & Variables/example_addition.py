# Simple example of addition in python using the input from the user.

num1 = input("Enter first number:")
num2 = input("Enter second number:")
sum = float(num1) + float(num2) # here we are using float to convert into float value because input function takes input as float value too.
print("The sum of given numbers is:", sum)

